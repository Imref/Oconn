    private void szolgValasztActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_szolgValasztActionPerformed
        if (szolgList.getSelectedValue().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Nem választottál szolgáltatást!");
        } else {
            Vector<String> fejlec = new Vector<>();
            fejlec.add("Azonosító");
            fejlec.add("Csomag neve");
            fejlec.add("Havi díj");

            String valasztott = szolgList.getSelectedValue();

            if (valasztott.equals("TV")) {
                ATv tv = new ATv();
                Vector szolgCsom = new Vector();
                szolgCsom = tv.csomagok();
                csomagok.setModel(new DefaultTableModel(szolgCsom, fejlec));
            } else if (valasztott.equals("Telefon")) {
                ATelefon tv = new ATelefon();
                Vector szolgCsom = new Vector(tv.csomagok());
                csomagok.setModel(new DefaultTableModel(szolgCsom, fejlec));
            } else if (valasztott.equals("Internet")) {
                ANet tv = new ANet();
                Vector szolgCsom = new Vector(tv.csomagok());
                csomagok.setModel(new DefaultTableModel(szolgCsom, fejlec));
            }

        }
    }//GEN-LAST:event_szolgValasztActionPerformed

    private void SzerzSzelektActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SzerzSzelektActionPerformed
        String sz = tfValasztottSzerzodes.getText();
        ASzerzodes szam = new ASzerzodes();
        if (sz.isEmpty()) {
            int szersz = szam.generalSzerSzam();

            tfValasztottSzerzodes.setText(String.valueOf(szersz));
            kiSzerSzam.setText(String.valueOf(szersz));
        } else {
            kiSzerSzam.setText(sz);
        }


    }//GEN-LAST:event_SzerzSzelektActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (tfValasztottSzerzodes.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Nem választottál csomagot!");
        } else {
            ASzerzodes szerz = new ASzerzodes();
            int szerszam1 = Integer.parseInt(kiSzerSzam.getText());
            int csomag1 = Integer.parseInt(tfvalasztottCsomag.getText());
            int folyo1 = Integer.parseInt(tfValasztott.getText());

            szerz.felveszSzolgaltatas(szerszam1, csomag1, folyo1);
            Kedvezmeny kedv = new Kedvezmeny();
            kedv.kedvezmenyBeallit(szerszam1);
            this.dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if (tfValasztottSzerzodes.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Nem választottál csomagot!");
        } else {
            ASzerzodes szerz1 = new ASzerzodes();
            int szerszam1 = Integer.parseInt(kiSzerSzam.getText());
            int csomag1 = Integer.parseInt(tfvalasztottCsomag.getText());
            int folyo1 = Integer.parseInt(tfValasztott.getText());
            Egyenleg egy = new Egyenleg();
            double egyen = egy.egyenleg(szerszam1);
            if (egyen < 0) {
                JOptionPane.showMessageDialog(rootPane, "Tartozásod van ezen a " + kiSzerSzam.getText()
                        + " számú szerződéseden!\n Addig nem mondhatsz le szolgáltatást! FIZESS!!");
            } else {
                szerz1.torolSzolgaltatas(szerszam1, csomag1);
                Kedvezmeny kedv = new Kedvezmeny();
                kedv.kedvezmenyBeallit(szerszam1);
                this.dispose();
            }

        }
    }//GEN-LAST:event_jButton4ActionPerformed

