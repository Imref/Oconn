    private void executeHQLQuery(String hql) {

        Session session = NewHibernateUtil.getSessionFactory().openSession();
        try {

            session.beginTransaction();
            Query q = session.createQuery(hql);
            List list = q.list();
            displayResult(list);
            session.getTransaction().commit();
            System.out.println(list.size());

        } catch (HibernateException he) {
            he.printStackTrace();
        } finally {
            session.close();
        }
    }

    private void displayResult(List list) {
        Vector<String> fejlec = new Vector<>();
        Vector<Object> adatok = new Vector<>();
        fejlec.add("Folyószámla");
        fejlec.add("Név");
        fejlec.add("Cím");

        for (Object object : list) {
            Ugyfel u = (Ugyfel) object;
            Vector<Object> egySor = new Vector<>();
            egySor.add(u.getFolyosz());
            egySor.add(u.getNev());
            egySor.add(u.getCim());

            adatok.add(egySor);
            if (list.size() == 1) {
                tfValasztott.setText((Integer.toString(u.getFolyosz())));

            } else {
                tfuzi.setText("Kérem válassza ki a folyószámlát és írja be az alábbi mezőbe");
            }

        }

        table.setModel(new DefaultTableModel(adatok, fejlec));

    }
